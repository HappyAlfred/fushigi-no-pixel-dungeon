package com.fushiginopixel.fushiginopixeldungeon.items.armor.glyphs;

import com.fushiginopixel.fushiginopixeldungeon.actors.Char;
import com.fushiginopixel.fushiginopixeldungeon.actors.EffectType;
import com.fushiginopixel.fushiginopixeldungeon.actors.buffs.Buff;
import com.fushiginopixel.fushiginopixeldungeon.actors.buffs.Charm;
import com.fushiginopixel.fushiginopixeldungeon.effects.Speck;
import com.fushiginopixel.fushiginopixeldungeon.items.armor.Armor;
import com.fushiginopixel.fushiginopixeldungeon.items.armor.Armor.Glyph;
import com.fushiginopixel.fushiginopixeldungeon.sprites.ItemSprite;
import com.fushiginopixel.fushiginopixeldungeon.sprites.ItemSprite.Glowing;
import com.watabou.utils.Random;

public class Affection extends Glyph {
	
	private static ItemSprite.Glowing PINK = new ItemSprite.Glowing( 0xFF4488 );
	
	@Override
	public float procSufferAttack( Armor armor, Object attacker, Char defender, int damage , EffectType type ) {

		int level = Math.max(0, armor.level());

		if (attacker != null && attacker instanceof Char) {
			if (Random.Int(level / 2 + 100) >= 88) {

				Char at = (Char) attacker;
				int duration = Random.IntRange(2, 5);

				Buff.affect(at, Charm.class, duration, new EffectType(0, EffectType.SPIRIT)).object = defender.id();
				at.sprite.centerEmitter().start(Speck.factory(Speck.HEART), 0.2f, 5);

			}
		}
		
		return 1;
	}

	@Override
	public Glowing glowing() {
		return PINK;
	}
}

package com.fushiginopixel.fushiginopixeldungeon.items.stones;

import com.fushiginopixel.fushiginopixeldungeon.effects.Enchanting;
import com.fushiginopixel.fushiginopixeldungeon.effects.Speck;
import com.fushiginopixel.fushiginopixeldungeon.items.Item;
import com.fushiginopixel.fushiginopixeldungeon.items.armor.Armor;
import com.fushiginopixel.fushiginopixeldungeon.items.weapon.Weapon;
import com.fushiginopixel.fushiginopixeldungeon.messages.Messages;
import com.fushiginopixel.fushiginopixeldungeon.sprites.ItemSpriteSheet;
import com.fushiginopixel.fushiginopixeldungeon.utils.GLog;
import com.fushiginopixel.fushiginopixeldungeon.windows.WndBag;

public class StoneOfEnchantment extends InventoryStone {
	
	{
		mode = WndBag.Mode.ENCHANTABLE;
		image = ItemSpriteSheet.STONE_TIWAZ;
	}

	@Override
	public int price() {
		return 30 * quantity;
	}

	@Override
	protected void onItemSelected(Item item) {

		Item i = item;

		if (item instanceof Weapon) {
			
			((Weapon)item).enchant();
			
		} else {
			
			((Armor)item).inscribe();
			
		}
		
		curUser.sprite.emitter().start( Speck.factory( Speck.LIGHT ), 0.1f, 5 );
		Enchanting.show( curUser, item );


		if (item instanceof Weapon) {
			if(!((Weapon)i).equals((Weapon) item))
			GLog.p(Messages.get(this, "weapon"));
		} else {
			if(!((Armor)i).equals((Armor) item))
			GLog.p(Messages.get(this, "armor"));
		}

		useAnimation();
		
	}
}
